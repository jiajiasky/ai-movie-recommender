def get_mbti_group(mbti):
    """获取MBTI所属群体"""
    for group, types in MBTI_GROUPS.items():
        if mbti in types:
            return group
    return None


def get_movies_by_life_stage(life_stage):
    """获取指定人生阶段的所有电影"""
    movies = set()
    for movie, tags in MOVIE_LIFE_TAGS.items():
        if life_stage in tags:
            movies.add(movie)
    
    # 添加通用列表中的电影
    if life_stage in LIFE_STAGE_TAGS:
        movies.update(LIFE_STAGE_TAGS[life_stage])
    
    return list(movies)


def get_movies_by_mbti_group(mbti_group):
    """获取指定MBTI群体的所有电影"""
    return MOVIE_MBTI_GROUPS.get(mbti_group, [])


