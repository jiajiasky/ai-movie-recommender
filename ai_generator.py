    def _generate_recommendation(self, title, rating, style, life_msg) -> str:
        """生成个性化推荐语"""
        
        intro = style['intro']
        
        # 评分描述
        if rating >= 9.5:
            rating_desc = "神作级别，豆瓣评分高达"
        elif rating >= 9.0:
            rating_desc = "9分以上佳作，"
        else:
            rating_desc = "高分佳作，"
        
        # 组合推荐语
        if intro.endswith("？"):
            # ENFP 风格
            recommendation = f"{intro}\n\n{life_msg}，{rating_desc}{rating}分！"
        else:
            recommendation = f"{intro}，{life_msg}。{rating_desc}{rating}分，Top 50必看！"
        
        return recommendation
    
    def generate_for_user(self, mbti: str, life_stage: str, top_n: int = 5) -> list:
        """为用户生成推荐列表（含AI内容）"""
        
        import sys
        sys.path.insert(0, '.')
        from recommender import VibeRecommender
        
        recommender = VibeRecommender()
        recommender.set_user(mbti, life_stage)
        
        # 获取推荐
        results = recommender.recommend(top_n)
        
        # 为每部电影生成AI内容
        ai_results = []
        for r in results:
            movie = r['movie']
            ai_content = self.generate_story(movie['title_cn'], mbti, life_stage)
            ai_results.append({
                **r,
                'story': ai_content['story'],
                'recommendation': ai_content['recommendation']
            })
        
        return ai_results


